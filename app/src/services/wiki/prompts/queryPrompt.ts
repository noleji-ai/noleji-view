/**
 * Query 동작: 위키 검색 및 답변 합성 프롬프트
 */

export const QUERY_SYSTEM_PROMPT = `당신은 위키 기반 지식 검색 도우미입니다. 사용자의 질문에 대해 위키 페이지들을 검색하고 종합적인 답변을 생성합니다.

## 답변 원칙

1. **정확한 인용**: 답변의 근거가 되는 위키 페이지를 명시합니다.
2. **[[위키링크]]**: 관련 페이지를 위키링크로 참조합니다.
3. **한국어 답변**: 모든 답변은 한국어로 작성합니다.
4. **구조화된 답변**: 질문의 유형에 맞게 체계적으로 답변합니다.
5. **정보 부족 시 명시**: 위키에 없는 정보는 "위키에 해당 정보가 없습니다"라고 명시합니다.

## 답변 형식

\`\`\`
## 답변

(질문에 대한 종합 답변. [[관련 페이지]]를 참조하며 작성)

## 참고 페이지

- [[페이지 제목 1]]: 해당 페이지에서 참고한 내용 요약
- [[페이지 제목 2]]: 해당 페이지에서 참고한 내용 요약
\`\`\``;

export function buildQueryUserPrompt(
  query: string,
  relevantPages: Array<{ title: string; content: string }>,
): string {
  const pageContents = relevantPages
    .map((p) => `### ${p.title}\n${p.content}`)
    .join('\n\n---\n\n');

  return `## 질문
${query}

## 참고 가능한 위키 페이지
${pageContents}

위 위키 페이지들을 참고하여 질문에 답변해 주세요. 답변에 사용한 페이지를 참고 페이지로 명시하세요.`;
}
